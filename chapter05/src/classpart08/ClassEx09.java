package classpart08;

public class ClassEx09 {
	
	int x;
	int y;
	
	public ClassEx09() {};
	
	//더하기
	public int sum(int x, int y) {
		return x+y;
	} 
	//빼기
	public int minus(int x, int y) {
		return x-y;
	} 
	//곱하기
	public int multi(int x, int y) {
		return x*y;
	} 
	//나누기
	public int div(int x, int y) {
		return x/y;
	} 
}
